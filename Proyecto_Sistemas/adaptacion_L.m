% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
Z0=str2double(get(handles.edit2,'string'));
ZL=str2double(get(handles.edit1,'string'));
f=str2double(get(handles.edit3,'string'));
RL=real(ZL);
XL=imag(ZL);
if(Z0<ZL)
B1=(XL+abs(sqrt(RL/Z0)*sqrt(XL^2-RL*Z0+RL^2)))/(XL^2+RL^2);
X1=(Z0/RL)*(B1*(RL^2+XL^2)-XL);
B2=(XL-abs(sqrt(RL/Z0)*sqrt(XL^2-RL*Z0+RL^2)))/(XL^2+RL^2);
X2=(Z0/RL)*(B2*(RL^2+XL^2)-XL);
else
    X1=abs(sqrt(RL*(Z0-RL)))-XL;
    B1=(X1+XL)/((X1+XL)^2+RL^2);
    X2=-abs(sqrt(RL*(Z0-RL)))-XL;
    B2=(X2+XL)/((X2+XL)^2+RL^2);
end
if(B1<0)
    LB1=-(B1*2*pi*f)^-1
    CB1=NaN;
else
    CB1=B1/(2*pi*f);
    LB1=NaN;
end
set(handles.edit4,'string',abs(CB1));
if(B2<0)
    LB2=-(B2*2*pi*f)^-1;
    CB2=NaN;
else
    CB2=B2/(2*pi*f);
    LB2=NaN;
end 
set(handles.edit6,'string',abs(LB2));
if(X1<0)
    CX1=(X1*2*pi*f)^-1;
    LX1=NaN;
else
    LX1=X1/(2*pi*f);
    CX1=NaN;
end 
 set(handles.edit7,'string',abs(LX1));
  if(X2<0)
     CX2=(X1*2*pi*f)^-1;
     LX2=NaN;
 else
     LX2=X1/(2*pi*f)
     CX2=NaN;
  end 
  set(handles.edit5,'string',abs(CX2));





